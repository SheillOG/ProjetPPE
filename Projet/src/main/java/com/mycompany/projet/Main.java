    private void buttonModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonModifierActionPerformed

        DefaultListModel produit = (DefaultListModel) jList2.getModel();

        try {

            ResultSet lesDonnees = SqlManager.getInstance().requeteSelection("select * from client");

            if (jList2.getSelectedValue() == null) {
                JOptionPane.showMessageDialog(null, "Veuillez selectionner une categorie");
                return;
            } else {
                ClientSauvegarde.setVisible(true);
                ClientSauvegarde.setSize(400, 400);
                ClientSauvegarde.setResizable(false);
                ClientSauvegarde.setLocationRelativeTo(null);
            }

            while (lesDonnees.next()) {
                String nomClient = lesDonnees.getString("nomClient");
                if (nomClient.equalsIgnoreCase(jList2.getSelectedValue())) {

                    String emailClient = lesDonnees.getString("emailClient");
                    String telephoneClient = lesDonnees.getString("telephoneClient");
                    String adresse = lesDonnees.getString("adresse");
                    saisieModificiationNom.setText(nomClient);
                    saisieModificiationEmail.setText(emailClient);
                    saisieModificiationTelephone.setText(telephoneClient);
                    saisieModificiationAdresse.setText(adresse);

                }

            }
        } catch (SQLException ex) {
            Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_buttonModifierActionPerformed

